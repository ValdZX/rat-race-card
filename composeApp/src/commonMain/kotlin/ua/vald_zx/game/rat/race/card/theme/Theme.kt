internal val LocalThemeIsDark = compositionLocalOf { mutableStateOf(true) }

@Composable
internal fun AppTheme(
    content: @Composable() () -> Unit
) {
    val systemInDarkTheme = isSystemInDarkTheme()
    val isDarkState = remember { mutableStateOf(systemInDarkTheme) }
    val darkTheme by isDarkState
    val colors = if (darkTheme) DarkIfobsColors else LightIfobsColors
    val colorPalette = remember {
        AppColors(
            cash = colors.cash,
            positive = colors.positive,
            negative = colors.negative,
            action = colors.action,
            buy = colors.buy,
            family = colors.family,
            funds = colors.funds,
            isDark = colors.isDark,
        )
    }
    colorPalette.update(colors)
    CompositionLocalProvider(
        LocalThemeIsDark provides isDarkState,
        LocalAppColors provides colorPalette
    ) {
        SystemAppearance(!darkTheme)

        MaterialTheme(
            colorScheme = if (darkTheme) darkScheme else lightScheme,
            content = { Surface(content = content) }
        )
    }
}

object AppTheme {
    val colors: AppColors
        @Composable
        get() = LocalAppColors.current
}

@Composable
internal expect fun SystemAppearance(isDark: Boolean)
